# List items are indexed and you can access them by referring to the index number:



students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[0])


students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[-1])

students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[-2])

students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[0:3])


students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[:3])

students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[2:])


students = ["Zafar", "ali", "mansour", "khan", "sultan", "jan"]

print(students[-3:-1])

