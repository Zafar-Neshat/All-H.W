# Perform access list, change list, add list and remove list methodes.


# Removing 
my_list = [1, 2, 3, 4, 5]
my_list.pop(2)

print(my_list)


# Adding 
my_list = [1, 2, 3, 4, 5]
my_list.append(6)

print(my_list)


# Changing 
my_list = [1, 2, 3, 4, 5]
my_list[3] = 10

print(my_list)


# Accessing 
my_list = [1, 2, 3, 4, 5]
print(my_list[0])

print(my_list[-3])

print(my_list[1:4])
