# Make python function to perform +, -, * and / operation with logical data in python 



# *
def calculate_rectangle_area(width, height):
    print(width * height)

calculate_rectangle_area(5, 8)


# +
def Sum_the_numbers(Salary, Overtime):
    print(Salary + Overtime)

Sum_the_numbers(1000, 800)

# -
def minus_the_numbers(Salary, Finance):
    print(Salary - Finance)

minus_the_numbers(10000, 500)


# /
def dividing_the_numbers(salary, home_car):
    print(salary / home_car)

dividing_the_numbers(5000, 200)